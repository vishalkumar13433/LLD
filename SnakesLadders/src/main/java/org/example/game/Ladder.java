package org.example.game;

public class Ladder {
    public int bottom;
    public int top;

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public Ladder(int bottom, int top) {
        this.bottom = bottom;
        this.top = top;
    }
}
